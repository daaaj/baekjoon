package baekjoon;

import java.util.Scanner;

public class Level2 {

	public static void main(String[] args) {
		
		// 시험 점수를 입력받아
		// 90 ~ 100점은 A,
		// 80 ~ 89점은 B, 
		// 70 ~ 79점은 C, 
		// 60 ~ 69점은 D, 
		// 나머지 점수는 F를 출력하는 프로그램을 작성하시오.
		
		Scanner sc = new Scanner(System.in);
		
		int i = sc.nextInt();
		
		if(i >= 90) {
			System.out.println("A");
		} else if(i >= 80) {
			System.out.println("B");
		} else if(i >= 70) {
			System.out.println("C");
		} else {
			System.out.println("D");
		}
		
		
	}
}
